'''
    [미지수가 양쪽에 있는 일차부등식]
    다음 부등식의 풀이 과정을 주석으로 표현하시오.	
'''

b = 5.25
print(b - 4.5 >= b * 0.2 - 0.3)
'''
b - 4.5 >= 0.2b - 0.3
b - 0.2b >= -0.3 + 4.5
0.8b >= 4.2
8b >= 42
b >= 5.25
'''

c = 3
print(c * 2 - 3 > c - 3 + 2)
'''
2c - 3 > c -1
2c - c > -1 + 3
c > 2
'''

d = 3
print(8 - 2 * d < d + 2)
'''
8 -2d < d + 2
-2d - d < 2 - 8
-3d < -6
d > 2
'''

e = -2.7
print(4 * (5 + e) <= -2 * (e - 2))
'''
20 + 4e <= -2e + 4
4e + 2e <= 4 - 20
6e <= -16
e <= -2.66...
'''