'''
	[문제1]
		a리스트의 값들을 한 칸씩 
		앞으로 당기고 출력하시오.
	[정답1]
		a = [10,20,30,40,50,0]
  
	[문제2]
		b리스트의 값들을 한 칸씩 
		뒤로 밀고 출력하시오.
	[정답2]
		b =  [0,10,20,30,40,50]
'''

# [문제1]
a = [0, 10, 20, 30, 40, 50]
for i in range(len(a) - 1):
	a[i] = a[i + 1]
a[len(a) - 1] = 0
print("a =", a)

# [문제2]
b =  [10, 20, 30, 40, 50, 0]

index = len(b) - 1
for i in range(len(b) - 1):
	b[index] = b[index - 1]
	index -= 1
b[0] = 0
print("b =", b)






