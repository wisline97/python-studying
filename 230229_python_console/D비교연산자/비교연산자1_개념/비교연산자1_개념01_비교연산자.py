'''
	[1] 비교 연산자 
		- 비교 연산자의 결과는 참(True) 또는 거짓(False)이다.
		- 아래 비교는 왼쪽을 기준으로한다.
		(1) 크니? 		    10 > 3
		(2) 작니?			10 < 3
		(3) 같니?			10 == 3
		(4) 다르니?		    10 != 3
		(5) 크거나 같니?	10 >= 3
		(6) 작거나 같니?	10 <= 3
'''

print(10 > 3) 	# True
print(10 < 3) 	# False
print(10 == 3) 	# False
print(10 != 3) 	# True
print(10 >= 3) 	# True
print(10 <= 3) 	# False

