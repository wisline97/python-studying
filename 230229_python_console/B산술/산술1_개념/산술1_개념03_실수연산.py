# 소수점 제한 명령어 
 
# 반올림 : round
# round( 실수 , 자리수 )
print("실수 출력 : " , 10 / 3)
print("실수 출력(round) 자리수한자리 : " , round(10 / 3 , 1)) 

print("실수 출력(round) 자리수두자리 : " , round(10 / 3 , 2)) 


print("---------------------------------------")
print("실수 출력 : " , 10.3 + 2.3)
print("실수 출력(round) : " , round(10.3 + 2.3, 1)) 
