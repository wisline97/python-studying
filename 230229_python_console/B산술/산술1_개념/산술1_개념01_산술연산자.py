# 연산자(operator)

# 산술 연산자 종류 (6가지)

# 1) 더하기            +
# 2) 빼기              -
# 3) 곱하기            *
# 4) 나누기            /   
# 5) 나누기(몫)        //
# 6) 나누기(나머지)    %


print(10 + 3) # 더하기
print(10 - 3) # 빼기
print(10 * 3) # 곱하기
print(10 / 3) # 나누기(소수점이 생길수있다.) 
# 컴퓨터는 실수를 정확히 계산할 수 없다. 
# 적당한 선에서 반올림을해 소수점을 표현한다.
print(10 // 3) # 몫 (소수점이 제거된다.)
print(10 % 3) # 나머지 (몫을제거한 나머지.)


