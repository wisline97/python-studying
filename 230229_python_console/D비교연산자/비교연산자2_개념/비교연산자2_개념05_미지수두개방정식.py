'''
    [미지수가 양쪽에 있는 일차방정식]
    다음방정식의 풀이과정을 주석으로 표현하시오.
'''	
    
a = 0
print(5 * a - 2 == 3 * (4 * a + 4))

'''
[풀이]
    5 * a - 2 == 3 * (4 * a + 4)

    5a - 2 == 3 * (4a + 4)
    5a - 2 == 12a + 12

    5a - 12a - 2 = 12a - 12a + 12

    -7a - 2 = 12
    -7a - 2 + 2 = 12 + 2
    -7a = 14
    
    -7a / 7 = 14 / 7
    -a = 2
    a = -2
'''
a = -2
print(5 * a - 2 == 3 * (4 * a + 4))