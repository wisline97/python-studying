'''
	[ 속력 문제 ]		
		[1] 철수는 시속 3km 의 속도로 37분간 걸어갔다. 
			철수가 간거리는 몇km 인가?
			철수가 간거리는 몇m 인가?

			60분 : 3km = 37분 : nkm
			60 x n = 3 x 37
			n = 3km x 37 / 60
			n = 3000m x 37 / 60
			    
		[2] 철수는 870미터를 40분간 걸어갔다. 
			철수의 시속은 얼마인가?
			철수의 분속은 얼마인가?

			40분 / 60 : 870m = 1시간 : nM
			n x (40 / 60) = 870 x 1
			n = 870 / (40 / 60)
			n = 870 / 40
'''

print("[문제1]")
print(3 * 37 / 60)
print(3000 * 37 / 60)

print("[문제2]")
print(870 / (40 / 60))
print(870 / 40 )